/** 
Código demonstra a aplicação de operadores de atribuição
*@author Carlos Dalagrana*/

	public class Operador5{
	
		public static void main (String [] args){
		
		//Adiciona um valor a variável x
		int x = 7;
		x += 3;
		System.out.println(x);
		
		//Subtrai um valor a variável x1
		int x1 = 7;
		x1 -= 3;
		System.out.println(x1);
		
		//Multiplica um valor a variável x2
		int x2 = 7;
		x2 *= 3;
		System.out.println(x2);
		
		//Divide um valor pela variável x3
		int x3 = 7;
		x3 /= 3;
		System.out.println(x3);
		
		//Apresenta o resto de uma divisão feita pela variável x4
		int x4 = 7;
		x4 %= 3;
		System.out.println(x4);
		}
	}