/** 
Código que apresenta cálculos com operadores de comparação
*@author Carlos Dalagrana*/

	public class Operador3{
		public static void main(String [] args){
		
		//Operador que verifica se os operandos são iguais
		int x = 7;
		System.out.println (x==7);
		
		//Operador que verifica se os operandos são diferentes
		int x1 = 7;
		System.out.println (x1!=7);
		
		//Operador que verifica se o operando é igual ou maior
		int x2 = 7;
		System.out.println (x2>=7);
		
		//Operador que compara se o tipo é uma instância da classe Wrapper
		Integer x3 = 7;
		System.out.println (x3 instanceof Integer);
		
		}
	
	
	}